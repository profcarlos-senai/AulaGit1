public class Gato {
    private String nome;
    private int patas;
    private String cor;

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setPatas(int patas) {
        this.patas = patas;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getNome() {
        return nome;
    }

    public int getPatas() {
        return patas;
    }

    public String getCor() {
        return cor;
    }
}
